function changebackground() {
  var magicNumber = 1;
  var magicNumber1 = 2;
  var magicNumber2 = 3;

  if (magicNumber === 1) {
    paintYellowRectangle();
  }
  if (magicNumber1 === 2) {
    paintGreenRectangle();
  }
  if (magicNumber2 === 3) {
    paintRedRectangle();
  }
}

changebackground();
